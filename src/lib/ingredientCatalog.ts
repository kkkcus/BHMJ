// 동의어/표기 통일
const ALIAS: Record<string, string> = {
  "달걀": "계란",
  "파": "대파",
  "쪽파": "대파",
  "스팸": "햄",
  "참치": "참치캔",
  "김": "김(구운김)",
};

export function normalizeIngName(name: string) {
  const n = name.trim();
  return ALIAS[n] ?? n;
}

